export const hardWords = [...easyWords, ...hardNumbers, ...hardSymbols];

export function generateText(
  mode: "easy" | "hard" | "custom",
  wordCount: number,
  customText?: string
): string {
  if (mode === "custom") {
    if (!customText || customText.trim() === "") {
      return "Please enter your custom text in the text area above to start typing.";
    }
    return customText.trim();
  }

  const wordArray = mode === "easy" ? easyWords : hardWords;
  const result: string[] = [];

  for (let i = 0; i < wordCount; i++) {
    const randomIndex = Math.floor(Math.random() * wordArray.length);
    result.push(wordArray[randomIndex]);
  }

  return result.join(" ");
}
